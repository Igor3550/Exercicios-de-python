# estruturas de laço de repetição
for c in range(0, 3):
    print(c)
print('')
for c in range(1, 3+1):
    print(c)
print('')
for c in range(5, 1, -1):
    print(c)
print('')
s = 0
for c in range(0, 4):
    n = int(input('Digite um numero: '))
    s += n
print('O somatorio de todos os numeros foi {}'.format(s))
print('')
